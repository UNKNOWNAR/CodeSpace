ll power(ll base, ll exp, ll mod) {
    ll res = 1;
    base %= mod;
    while (exp > 0) {
        if (exp % 2 == 1) res = (res * base) % mod;
        base = (base * base) % mod;
        exp /= 2;
    }
    return res;
}

void solve() {
    ll n,m,r,c;
    cin>>n>>m>>r>>c;
    ll mod = 998244353;
    ll free = (r-1)*m + (c-1)*n - (r-1)*(c-1);
    ll ans = power(2, free, mod);
    cout<<ans<<endl;
}

